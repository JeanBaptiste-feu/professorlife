public class ProfessorStats {

    String name;
    int age;

    /**
     * Player embodies a professor managing student
     * This class describe professor statistics
     * @param nameParam
     *      name of player's professor
     * @param ageParam
     *      age of player's professor
     */
    public ProfessorStats(String nameParam, int ageParam){
        this.name = nameParam;
        this.age = ageParam;
    }
}